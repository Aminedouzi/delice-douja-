/**
 * Creates / updates .env.local with Supabase keys (safe for secrets; gitignored by default).
 * Run: node scripts/init-supabase-env.mjs
 */
import * as fs from "fs";
import * as path from "path";
import * as readline from "readline";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const target = path.join(root, ".env.local");

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout,
});

function ask(question) {
  return new Promise((resolve) => rl.question(question, resolve));
}

function esc(value) {
  return JSON.stringify(value ?? "");
}

console.log(`
=== Supabase env setup ===
Open: https://supabase.com/dashboard/project/kgyeyjxvtoumrjpzmdnt/settings/api
Copy "anon" "public" and (optional) "service_role" (Reveal).
`);

const url =
  (await ask(`Project URL [https://kgyeyjxvtoumrjpzmdnt.supabase.co]: `)).trim() ||
  "https://kgyeyjxvtoumrjpzmdnt.supabase.co";

const anon = (await ask("Paste anon public key (long JWT, starts with eyJ): ")).trim();
if (!anon || anon.length < 20) {
  console.error("Anon key looks too short or empty. Run again and paste the full key.");
  rl.close();
  process.exit(1);
}

const service = (
  await ask("Paste service_role key (optional, Enter to skip — needed for admin CRUD): ")
).trim();

const block = [
  "# Generated by scripts/init-supabase-env.mjs — do not commit if it contains secrets",
  `NEXT_PUBLIC_SUPABASE_URL=${esc(url)}`,
  `NEXT_PUBLIC_SUPABASE_ANON_KEY=${esc(anon)}`,
  service ? `SUPABASE_SERVICE_ROLE_KEY=${esc(service)}` : "# SUPABASE_SERVICE_ROLE_KEY not set — admin product writes may fail (RLS)",
  "",
].join("\n");

fs.writeFileSync(target, block, "utf8");
console.log(`\nWrote ${path.relative(root, target)}`);
console.log("Stop your dev server (Ctrl+C), then run: npm run dev\n");
rl.close();
